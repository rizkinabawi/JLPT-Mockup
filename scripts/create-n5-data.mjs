// ── Flatten S2 passages into individual questions ─────────────────────────────
function flattenPassages(passages) {
  const flat = [];
  for (const p of passages) {
    for (const q of p.questions) {
      flat.push({
        passage: p.passage,
        question: q.q,
        choices: q.c,
        correct_answer: q.a,
        correct_text: q.t,
      });
    }
  }
  return flat;
}

// ── Build source packets ──────────────────────────────────────────────────────
function buildSourcePacket(packNum, rngSeed) {
  const rng = makeRng(rngSeed);

  const s0 = shuffle(S0_POOL, makeRng(rngSeed + 1));
  const s1 = shuffle(S1_POOL, makeRng(rngSeed + 2));
  const s2flat = flattenPassages(S2_POOL);
  const s2 = shuffle(s2flat, makeRng(rngSeed + 3));

  // Source packet: take ~80% of each section
  const s0q = s0.slice(0, 36);
  const s1q = s1.slice(0, 38);
  const s2q = s2.slice(0, 18);

  let num = 1;
  const sections = [
    {
      title: "文字・語彙",
      questions: s0q.map(q => ({
        number: num++,
        question: `${num - 1}. ${q.q}`,
        choices: q.c,
        correct_answer: q.a,
        correct_text: q.t,
      })),
    },
    {
      title: "語彙・文法",
      questions: s1q.map(q => ({
        number: num++,
        question: `${num - 1}. ${q.q}`,
        choices: q.c,
        correct_answer: q.a,
        correct_text: q.t,
      })),
    },
    {
      title: "読解",
      questions: s2q.map(q => {
        const n = num++;
        return {
          number: n,
          question: `${n}. 「${q.passage}」\n\n${q.question}`,
          choices: q.choices,
          correct_answer: q.correct_answer,
          correct_text: q.correct_text,
        };
      }),
    },
  ];

  const totalQ = sections.reduce((s, sec) => s + sec.questions.length, 0);
  return {
    level: "N5",
    exam_number: packNum,
    total_questions: totalQ,
    sections,
    generated_at: new Date().toISOString(),
    mix_version: "v1",
  };
}

// ── Seeded-shuffle mix generator (same as generate-mixed-packets.mjs) ──────────
function makeRng2(seed) {
  let s = (seed >>> 0) || 1;
  return () => {
    s = Math.imul(s, 1664525) + 1013904223;
    return (s >>> 0) / 0x100000000;
  };
}
function shuffleArr(arr, rng) {
  const a = [...arr];
  for (let i = a.length - 1; i > 0; i--) {
    const j = Math.floor(rng() * (i + 1));
    [a[i], a[j]] = [a[j], a[i]];
  }
  return a;
}

function buildMixedPackets(srcPackets, numPackets) {
  // Pool by section index, deduplicated by question text prefix
  const pools = [];
  srcPackets.forEach(exam => {
    exam.sections.forEach((sec, si) => {
      if (!pools[si]) pools[si] = [];
      (sec.questions || []).forEach(q => pools[si].push(q));
    });
  });
  const dedupedPools = pools.map(pool => {
    const seen = new Set();
    return pool.filter(q => {
      const key = q.question.slice(0, 80);
      if (seen.has(key)) return false;
      seen.add(key);
      return true;
    });
  });

  console.log(`[N5 mix] Pool sizes: ${dedupedPools.map((p, i) => `S${i}:${p.length}`).join("  ")}`);

  const pickCounts = [28, 30, 14];  // 72 per mixed packet
  const packets = [];

  for (let pIdx = 0; pIdx < numPackets; pIdx++) {
    let globalNum = 1;
    const sections = dedupedPools.map((pool, si) => {
      const pick = pickCounts[si];
      const seed = (pIdx + 1) * 7919 + si * 31337 + 78; // 78 = 'N'
      const sampled = shuffleArr(pool, makeRng2(seed)).slice(0, pick);

      // Fix numbering
      const questions = sampled.map(q => {
        const n = globalNum++;
        // Replace leading "NN. " number in question text
        const qText = q.question.replace(/^\d+\.\s*/, `${n}. `);
        return { ...q, number: n, question: qText };
      });

      return {
        title: ["文字・語彙", "語彙・文法", "読解"][si] || `Section ${si + 1}`,
        questions,
      };
    });

    const totalQ = sections.reduce((s, sec) => s + sec.questions.length, 0);
    packets.push({
      level: "N5",
      exam_number: pIdx + 1,
      total_questions: totalQ,
      sections,
      generated_at: new Date().toISOString(),
      mix_version: "v1",
    });
    console.log(`  Mixed packet ${pIdx + 1}: ${totalQ} questions`);
  }
  return packets;
}

